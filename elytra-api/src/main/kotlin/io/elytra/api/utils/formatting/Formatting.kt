package io.elytra.api.utils.formatting

/**
 * Enumeration of the text format operations supported by the client
 */
object Formatting {
	const val OBSFUSCATED = "§k"

	const val BOLD = "§l"

	const val STRIKETHROUGH = "§m"

	const val UNDERLINE = "§n"

	const val ITALIC = "§o"

	const val RESET = "§r"

	const val NEW_LINE = "\\\\n"
}
